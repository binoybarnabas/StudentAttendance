        <p>Master's degree programs are graduate programs that let students specialize in an area of study. They typically take 1-2 years to complete. Along with an undergraduate degree, enrolling in a master's degree program usually requires a minimum GPA and an acceptance score on a graduate entrance exam, such as the Graduate Record Examination (GRE). Many master's degree programs require a thesis or capstone project for graduation. </p>
